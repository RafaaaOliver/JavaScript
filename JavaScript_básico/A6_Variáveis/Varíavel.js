let nome = 'João';
console.log(nome) // comando para declarar a variável

let test;
console.log(test) // a variável pode ser declarada sem conteúdo

let arroba; // declara a variável
arroba = 'Rafael'; // Inicializa a variável (insere conteúdo)
console.log(arroba)

arroba = '12456'; // substituindo o conteúdo da variável arroba
console.log(arroba) 

